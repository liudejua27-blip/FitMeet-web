export function classifyWorkoutIntent(
  message: string,
): 'workout' | 'negative' | 'unknown' {
  const text = message.trim();

  if (
    /(不要|不想|先不|暂时不|不用).{0,12}(找人|约练|匹配|搭子|推荐)/i.test(text)
  ) {
    return 'negative';
  }

  if (
    /(?:约练|约跑|约球|约.{0,4}(球|打球|羽毛球|篮球|网球)|运动搭子|训练搭子|跑步搭子|健身搭子|羽毛球搭子|篮球搭子|网球搭子|瑜伽搭子|游泳搭子|一起.{0,12}(跑步|运动|训练|健身|撸铁|羽毛球|篮球|网球|瑜伽|游泳|散步|徒步|骑行)|找.{0,16}(跑步|运动|训练|健身|撸铁|羽毛球|篮球|网球|瑜伽|游泳|散步|徒步|骑行)(?:的人|搭子|伙伴)?|(?:跑步|运动|训练|健身|撸铁|羽毛球|篮球|网球|瑜伽|游泳|散步|徒步|骑行).{0,8}(搭子|伙伴)|附近.{0,16}(跑步|运动|训练|健身|撸铁|搭子|伙伴)|有人.{0,16}(一起|陪).{0,16}(跑步|运动|训练|健身|撸铁|打球|练))/i.test(
      text,
    )
  ) {
    return 'workout';
  }

  if (
    /(跑步|慢跑|健身|撸铁|羽毛球|篮球|网球|散步|徒步|骑行|瑜伽|游泳|训练|运动)/i.test(
      text,
    ) &&
    /(今天|今晚|明天|周末|上午|中午|下午|晚上|附近|大学|公园|体育馆|健身房|球馆|操场|青岛|北京|上海|杭州|深圳|广州)/i.test(
      text,
    )
  ) {
    return 'workout';
  }

  return 'unknown';
}
