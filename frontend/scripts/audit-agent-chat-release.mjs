const fail = (message) => {
  console.error(`[agent-chat-release-audit] FAIL: ${message}`);
  process.exitCode = 1;
};

const gitTracked = (relativePath) => {
  if (!existsSync(path.join(rootDir, '.git'))) return true;
  const result = spawnSync('git', ['ls-files', '--error-unmatch', relativePath], {
    cwd: rootDir,
    stdio: 'ignore',
  });
  return result.status === 0;
};

const gitStatusLine = (relativePath) => {
  if (!existsSync(path.join(rootDir, '.git'))) return '';
  const result = spawnSync('git', ['status', '--short', '--', relativePath], {
    cwd: rootDir,
    encoding: 'utf8',
  });
  return result.status === 0 ? result.stdout.trim() : '';
};

const gitTrackedEntries = (pathspec) => {
  if (!existsSync(path.join(rootDir, '.git'))) return [];
  const result = spawnSync('git', ['ls-files', pathspec], {
    cwd: rootDir,
    encoding: 'utf8',
  });
  return result.status === 0
    ? result.stdout
        .split('\n')
        .map((line) => line.trim())
        .filter(Boolean)
    : [];
};

for (const relativePath of requiredTrackedFiles) {
  const absolutePath = path.join(rootDir, relativePath);
  if (!existsSync(absolutePath)) {
    fail(`missing required assistant-ui file: ${relativePath}`);
    continue;
  }
  if (!gitTracked(relativePath)) {
    fail(`required assistant-ui file is not tracked by git: ${relativePath}`);
  }
  const status = gitStatusLine(relativePath);
  if (/^(D.|.D)/.test(status)) {
    fail(`required assistant-ui file is marked deleted in git status: ${status}`);
  }
}

for (const relativePath of forbiddenPresentFiles) {
  if (existsSync(path.join(rootDir, relativePath))) {
    fail(`legacy Agent file must not exist in release source: ${relativePath}`);
  }
  const status = gitStatusLine(relativePath);
  if (/^[AMRC]|\sA/.test(status)) {
    fail(`legacy Agent file must not be staged or re-added: ${status}`);
  }
}

for (const prefix of forbiddenTrackedPrefixes) {
  const tracked = gitTrackedEntries(prefix);
  if (tracked.length > 0) {
    fail(
      `release-only QA or temporary artifacts are tracked under ${prefix}: ${tracked.join(', ')}`,
    );
  }
}

const sourceRoots = [path.join(frontendDir, 'src'), path.join(frontendDir, 'scripts')];

const sourceExtensions = new Set(['.ts', '.tsx', '.js', '.jsx', '.mjs', '.css']);

const walkFiles = (directory) => {
  const entries = readdirSync(directory, { withFileTypes: true });
  return entries.flatMap((entry) => {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) return walkFiles(fullPath);
    return sourceExtensions.has(path.extname(entry.name)) ? [fullPath] : [];
  });
};

for (const sourceRoot of sourceRoots) {
  if (!existsSync(sourceRoot)) continue;
  for (const filePath of walkFiles(sourceRoot)) {
    const relativePath = path.relative(rootDir, filePath);
    if (allowedReferenceFiles.has(relativePath)) continue;
    const contents = readFileSync(filePath, 'utf8');
    for (const pattern of forbiddenSourcePatterns) {
      if (contents.includes(pattern)) {
        fail(`forbidden legacy Agent reference "${pattern}" found in ${relativePath}`);
      }
    }
  }
}

for (const check of requiredContentChecks) {
  const absolutePath = path.join(rootDir, check.file);
  if (!existsSync(absolutePath)) {
    fail(`missing required release evidence file: ${check.file}`);
    continue;
  }
  const contents = readFileSync(absolutePath, 'utf8');
  for (const pattern of check.patterns) {
    if (!contents.includes(pattern)) {
      fail(`required release evidence "${pattern}" missing from ${check.file}`);
    }
  }
}

if (process.exitCode) {
  process.exit(process.exitCode);
}

